#Arithmetic Operators:
a =int(input('please give one value: '))
b =int(input('please give second value'))
print("a+b:",'add')
print("a-b:",'sub')
print("a*b:",'multi')
print("a/b:",'div')
print("a//b:",'div')
print("a%b:",'remender')
print("a**b:",'power')

#Comparison Operators:
a =int(input('please give one value: '))
